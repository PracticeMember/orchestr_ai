
def multiply(a:int,b:int)->int:  
    """Multiply Two Integers"""
    return a * b

def add(a: int ,b:int)-> int:
    """Add Two Numbers"""
    return a+b

def divide(a:int,b:int)->int:
    """Divide Two Numbers"""
    if b==0:
        return "Cannot be divided by zero"
    return a/b


def subtract(a:int,b:int)->int:
    """Subtract Two Numbers"""
    return a-b
